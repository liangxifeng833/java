package com.soufang.esproject.lxf;

/**
 * 定义一个枚举类型
 */
public enum  Color {
    /**
     * 对于枚举类型来说, 以下三个常量代表的是类型为Color的枚举对象.
     *      RED是Color的一个对象
     *      GREEN也是Color的一个对象
     *      BLUE同样也是Color的一个对象.
     * 只不过这三个对象是在枚举类的内部事先给定好的, 其他的外面是不能在产生Color枚举类型对象的.
     * 也就是说枚举类型对象一定是在枚举类型内部来声明和定义的, 外部是不可以的.
     */
    RED, GREEN, BLUE;
}
